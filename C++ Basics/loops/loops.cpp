// for loop
#include <iostream>
using namespace std;
int main()
{

  // for
  for (int i = 0; i < 5; i++)
  {
    cout << "FOR : YOU KNOW WHO ELSE SUFFERS FROM DEMENTIA ?" << endl;
  }

  /*  infitine for loop
    for(;;){
     infinite !!!
 } */

  cout << "\n\n"
       << endl;

  // while

  int i = 0;
  while (i < 5)
  {
    cout << "WHILE : YOU KNOW WHO ELSE SUFFERS FROM DEMENTIA ?" << endl;
    i++;
  }

  cout << "\n\n"
       << endl;

  // do while
  i = 0;
  do
  {
    cout << "DO-WHILE : YOU KNOW WHO ELSE SUFFERS FROM DEMENTIA ?" << endl;
    i++;
  } while (i < 5);
}